// Single line , Multi line, Line Breaks, String Concatenation , Template Literals


//normal string output
console.log( "hello world") // Output: hello world

// Using String Concatenation
console.log("Hello from " + "Chattogram!"); // Output: Hello from Chattogram!

// Using Template Literal
console.log(`Hello from Chattogram!`); // Output: Hello from Chattogram!


// Multi-line with Line Breaks (\n) Using String Concatenation
console.log("Line one\nLine two\nLine three");
/* Output:
Line one
Line two
Line three */


//Multi-line with Line Breaks (\n)  Using Template Literal
console.log(`Line one\nLine two\nLine three`);
/* Output:
Line one
Line two
Line three */